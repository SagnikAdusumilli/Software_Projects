package com.GuI;
/**
 * 
 * @author Sagnik
 * stores x and y co-ordinates of a data point
 */
public class Point {
	private Number x;
	private Number y;

	public Point(Number x, Number y) {
		this.x = x;
		this.y = y;
	}

	public Number getX() {
		return x;
	}

	public Number getY() {
		return y;
	}
	
	
}
